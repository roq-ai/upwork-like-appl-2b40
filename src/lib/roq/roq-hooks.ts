export function useBugReportFindMany<
  T extends Prisma.bug_reportFindManyArgs,
  R extends GetFindResult<Prisma.$bug_reportPayload<DefaultArgs>, T>[],
>(
  args?: Prisma.SelectSubset<T, Prisma.bug_reportFindManyArgs>,
  options?: RequestOptions,
  swrOptions?: SWRRequestOptions<R>,
) {
  const roq = useRoqClient();
  const key = JSON.stringify(['useBugReportFindMany', args || {}]);
  return useSWR<R, Error>(
    key,
    async () => {
      const result = await roq.bug_report.findMany(args, options);
      return result as R;
    },
    {
      ...swrOptions,
      fallbackData: swrOptions?.initialData ?? swrOptions?.fallbackData,
    },
  );
}

